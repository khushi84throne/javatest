    private void jButton50ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton50ActionPerformed
        String answer;
        num2=Double.parseDouble(screen.getText());

        String stati =screen2.getText() + jButton50.getText();
        screen2.setText(stati);
        screenStatus.setText("Default");
        if(operator == "+"){
            ans = num1+num2;
            answer=String.format("%.2f" ,ans);
            screen.setText(answer);

            stati =screen2.getText() + answer;
            screen2.setText(stati);

        }else if(operator == "-"){
            ans = num1-num2;
            answer=String.format("%.2f" ,ans);
            screen.setText(answer);

            stati =screen2.getText() + answer;
            screen2.setText(stati);

        }else if(operator == "X" ){
            ans = num1*num2;
            answer=String.format("%.2f" ,ans);
            screen.setText(answer);

            stati =screen2.getText() + answer;
            screen2.setText(stati);

        }else if(operator == "/"){
            ans = num1/num2;
            answer=String.format("%.2f" ,ans);
            screen.setText(answer);

            stati =screen2.getText() + answer;
            screen2.setText(stati);
        }else if(operator == "%"){
            ans = num1%num2;
            answer=String.format("%.2f" ,ans);
            screen.setText(answer);

            stati =screen2.getText() + answer;
            screen2.setText(stati);
        }
    }//GEN-LAST:event_jButton50ActionPerformed

